def remove_emoji(text):
    emoji_pattern = re.compile(
        "["
        "\U0001F600-\U0001F64F"
        "\U0001F300-\U0001F5FF"
        "\U0001F680-\U0001F6FF"
        "\U0001F1E0-\U0001F1FF"
        "\U00002700-\U000027BF"
        "\U000024C2-\U0001F251"
        "]+",
        flags=re.UNICODE
    )
    return emoji_pattern.sub('', str(text))


def lemmatize_review(text):
    if pd.isna(text):
        return ''

    text = str(text).lower()
    text = remove_emoji(text)

    text = re.sub(r'[^а-яё\s]', ' ', text)

    tokens = text.split()

    tokens = [
        token for token in tokens
        if token not in stop_words and len(token) > 2
    ]

    lemmas = [
        morph.parse(token)[0].normal_form
        for token in tokens
    ]

    return ' '.join(lemmas)


