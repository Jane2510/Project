def clean_date(date_str):
    if pd.isna(date_str):
        return None

    date_str = str(date_str)
    now = datetime.now()

    if 'Сегодня' in date_str:
        return now.date()

    if 'Вчера' in date_str:
        return (now - timedelta(days=1)).date()

    try:
        date_part = date_str.split(',')[0]
        day, month_text = date_part.split()
        month = months[month_text]
        year = now.year
        return datetime.strptime(f'{year}-{month}-{day}', '%Y-%m-%d').date()
    except Exception:
        return None

def remove_sections(text):
    if pd.isna(text):
        return ''

    text = str(text)
    text = re.sub(
        r'(Достоинства|Недостатки|Комментарий):?',
        ' ',
        text,
        flags=re.IGNORECASE
    )
    text = re.sub(r'\s+', ' ', text)

    return text.strip()

