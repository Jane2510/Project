# словварь категорий проблем с их ключевыми словами

problem_categories = {
    "склеивание": [
        "склеивать", "склеить", "слипаться", "комок"
    ],

    "осыпание": [
        "осыпаться", "сыпаться"
    ],

    "нанесение": [
        "прокрашивать", "ложиться", "кисточка", "щёточка", "нанести", "неудобный"
    ]
}

# проверка отзыва на наличие ключевого слова из любой категории
def contains_keyword(review, keywords):
    words = review.split()

    for word in words:
        if word in keywords:
            return True

    return False

"""Индекс = кол-во отзывов с хотя бы одним словом категории / общее число отзывов на площадке с любой тональностью"""

results = []

platforms = df["Источник"].unique()

for category, keywords in problem_categories.items(): # метод items обращается и к ключу и к значению словаря

    for platform in platforms:
        # сколько отзывов на одной площадке
        df_platform = df[df["Источник"] == platform]
        total_reviews = len(df_platform)

        # сколько отзывов содержат любое слово из проблемных категорий
        count = 0

        for review in df_platform["Отзыв_предобработанный"].astype(str):
            if contains_keyword(review, keywords): # применяем пользовательскую функцию
                count += 1

        index = count / total_reviews

        # в пустой список подаем словари
        results.append({
            "Проблемная категория": category,
            "Площадка": platform,
            "Индекс": round(index, 2)
        })

result_df = pd.DataFrame(results)
